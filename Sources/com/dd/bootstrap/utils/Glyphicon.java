	public String glyphiconClassName(){
		return name().toLowerCase().replace("_", "-");
	}
	
	
	public static Glyphicon glyphiconForClassName(String className){
		Glyphicon glyph = valueOf(className);
		if(glyph==null){
		String value = ERXStringUtilities.capitalize(className.toLowerCase()).replace("-", "_");
			glyph = valueOf(value);
		}
		return glyph;
	}
	
	public String htmlCode(GeneralColor color){
		if(color==null){
		return "<span class=\"glyphicon glyphicon-"+glyphiconClassName()+"\" aria-hidden=\"true\"></span>";
		}
		return "<span class=\"glyphicon glyphicon-"+glyphiconClassName()+" "+color+"\" aria-hidden=\"true\"></span>";
	}
	
	
	public String htmlCode(){
		return htmlCode(null);
	}
	
	public void appendToResponse(WOResponse response, GeneralColor color){
		response.appendContentString(htmlCode(color));
	}
